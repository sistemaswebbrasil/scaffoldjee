package br.com.siswbrasil.scaffoldjee.exception;

public class ScaffoldBuilderException extends Exception {

	private static final long serialVersionUID = 1L;

	public ScaffoldBuilderException() {
		super();
	}

	public ScaffoldBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScaffoldBuilderException(String message) {
		super(message);
	}

	public ScaffoldBuilderException(Throwable cause) {
		super(cause);
	}	

}
